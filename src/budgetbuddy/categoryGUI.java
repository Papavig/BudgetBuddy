    private void getEntries(){
        javax.swing.table.DefaultTableModel table = (javax.swing.table.DefaultTableModel) Table.getModel();
        try{
            int rowCount = table.getRowCount();
            while(rowCount-- != 0){
                table.removeRow(0);
            }
            ResultSet rs = dbConnect.state.executeQuery("select * from category");
            
            int startNum = 0;
            while (rs.next()) {
                String data = rs.getString("category");
                Object obj[] = {++startNum,data};
                table.addRow(obj);
            }
        }catch(HeadlessException | SQLException ex){ //Exception handaler
            JOptionPane.showMessageDialog(null,ex);
        }
    }

