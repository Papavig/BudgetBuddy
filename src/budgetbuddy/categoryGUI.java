    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        try{
            String category = categoryText.getText();
            dbConnect.state.executeUpdate("insert into category value('"+category+"')");
            getEntries();
        }catch(HeadlessException | SQLException e){ //Exception handaler
            JOptionPane.showMessageDialog(null,"Error adding category: " + e.getMessage());
        }
        
    }//GEN-LAST:event_addActionPerformed

    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        int selectedRow = Table.getSelectedRow();
        String category = (String)Table.getValueAt(selectedRow, 1);
        try {
            dbConnect.state.executeUpdate("DELETE FROM category WHERE category = '" + category + "'");
            JOptionPane.showMessageDialog(null, "Category Removed!!");
            getEntries();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Error deleting category: " + e.getMessage());
        }
    }//GEN-LAST:event_removeActionPerformed

